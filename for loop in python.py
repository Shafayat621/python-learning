#anything itterable can be use for loop
#using range
for i in range(10):  #here range(10) means the loop will start from 0 and end in 10
    print(i)
print("\n")

for i in range(10,20):  #here range(10,20) means the loop will start from 10 and end in 19
    print(i)
print("\n")

for i in range(10,20,4):  #here range(10,20,4) means the loop will start from 10 and end in 19 & the difference will be 4,like 10,14 
    print(i)
print("\n")


